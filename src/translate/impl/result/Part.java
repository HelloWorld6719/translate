package translate.impl.result;

public class Part {

	private String part;
	private String means;

	public Part(String part, String means) {
		this.part = part;
		this.means = means;
	}

	public String getPart() {
		return part;
	}

	public void setPart(String part) {
		this.part = part;
	}

	public String getMeans() {
		return means;
	}

	public void setMeans(String means) {
		this.means = means;
	}

}
